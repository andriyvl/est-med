<h2>Toksyna botulinowa</h2>

&nbsp;



<p><strong>Toksyna botulinowa</strong>&nbsp;- jest bardzo silnie działającą naturalną toksyną powodującą zahamowanie uwalniania acetylocholiny - neuroprzekaźnika odpowiedzialnego za przekaźnictwo impuls&oacute;w między nerwami a mięśniami. Toksyna botulinowa działa poprzez blokowanie połączeń między zakończeniami nerw&oacute;w, a unerwianymi przez nie mięśniami, czyli blokuje tzw. synapsy nerwowo - mięśniowe. Nie dochodzi wtedy do skurcz&oacute;w porażonego mięśnia. Botulina jest r&oacute;wnie skuteczna u mężczyzn jak i u kobiet.</p>



<p>Toksynę botulinową stosuje się w medycynie w leczeniu r&oacute;żnych chor&oacute;b jak zez, choroby neurologiczne przebiegające z nadmiernym napięciem mięśni, jak r&oacute;wnież nadmierną potliwości. Do likwidacji zmarszczek mimicznych stosuje się bardzo małe stężenia toksyny, kt&oacute;re nie mają wpływu na inne elementy układu nerwowego poza okolicą w kt&oacute;rej podano zastrzyk.</p>



<h4>Czy zabieg jest bolesny?</h4>



<p>Wielu pacjent&oacute;w por&oacute;wnuje go do kilkusekundowego ugryzienia przez mr&oacute;wkę lub komara. Zabieg nie wymaga znieczulenia miejscowego. Ze względu na komfort pacjenta czasami stosuje się znieczulenie kremem (np. EMLA) na około 1 godzinę przed zabiegiem.</p>



<h4><strong>Kiedy powstaje i jak długo utrzymuje się efekt?</strong></h4>



<p>Efekt działania pojawia się po około 7 dniach i utrzymuje przez ok. 4-6 miesięcy. Zabieg w&oacute;wczas powinniśmy powt&oacute;rzyć.</p>



<h4>Schemat terapii</h4>



<p>Podanie toksyny botulinowej nie wymaga żadnych szczeg&oacute;łowych badań diagnostycznych. Botox jest wstrzykiwany przy użyciu jednorazowej strzykawki zaopatrzonej w bardzo cienką igłę. Małą ilość preparatu precyzyjnie wstrzykuje się w kilka miejsc na twarzy. Zabieg trwa kr&oacute;tko. W zależności od liczby leczonych miejsc (punkty iniekcji) zajmuje to około 15-20 minut. B&oacute;l związany ze wstrzykiwaniem jest minimalny.</p>



<p>Zabiegi można powtarzać co 6 - 8 miesięcy nie częściej niż co 3 miesiące. Jest to jeden z tych zabieg&oacute;w do kt&oacute;rych pacjenci wracają z ochotą, ponieważ daje bardzo szybkie i długotrwałe efekty, kt&oacute;re wszyscy wok&oacute;ł zauważają a jednocześnie jest to zabieg nieinwazyjny i prosty w wykonaniu.</p>



